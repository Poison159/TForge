/// <summary>
/// Class to encompass book type
/// </summary>
public class Book
{
    /// <summary>
    /// Primary key
    /// </summary>
    public int Id { get; set; }
    /// <summary>
    /// Title of book
    /// </summary>
    public string Title { get; set; } = "";
    /// <summary>
    /// Author of book
    /// </summary>
    public string Author { get; set; } = "";
}
